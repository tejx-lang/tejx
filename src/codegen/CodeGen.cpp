void CodeGen::emit(const std::string& code) {
    buffer << code;
}

void CodeGen::emitLine(const std::string& code) {
    for (int i = 0; i < indentLevel; i++) buffer << "    ";
    buffer << code << "\n";
}

void CodeGen::indent() { indentLevel++; }
void CodeGen::dedent() { indentLevel--; }


// Generate Anonymous Struct Definition
std::string CodeGen::genStructForSignature(const std::string& signature) {
    if (generatedStructs.count(signature)) {
        size_t hash = std::hash<std::string>{}(signature);
        return "Struct_" + std::to_string(hash);
    }
    
    // Parse signature "{key:type,key2:type}"
    // Strip { and }
    // Example: {name:string,age:number}
    std::string content = signature.substr(1, signature.length() - 2);
    std::vector<std::pair<std::string, std::string>> fields;
    
    std::stringstream ss(content);
    std::string segment;
    while (std::getline(ss, segment, ',')) {
        size_t colon = segment.find(':');
        if (colon != std::string::npos) {
            fields.push_back({segment.substr(0, colon), segment.substr(colon + 1)});
        }
    }
    
    size_t hash = std::hash<std::string>{}(signature);
    std::string structName = "Struct_" + std::to_string(hash);
    
    structBuffer << "struct " << structName << " {\n";
    // Fields
    for (auto& f : fields) {
        std::string cppType = "double";
        if (f.second == "string") cppType = "std::string";
        else if (f.second == "boolean") cppType = "bool";
        // Handle nested? Not now.
        structBuffer << "    " << cppType << " " << f.first << ";\n";
    }
    
    // Constructor from Map (for literals)
    structBuffer << "    " << structName << "(const std::map<std::string, tejx_runtime::Var>& m) {\n";
    for (auto& f : fields) {
        std::string cppType = f.second == "string" ? "std::string" : (f.second == "boolean" ? "bool" : "double");
        structBuffer << "        if (m.count(\"" << f.first << "\")) " 
                     << f.first << " = std::get<" << cppType << ">(m.at(\"" << f.first << "\"));\n";
    }
    structBuffer << "    }\n";
    
    // Default Constructor
    structBuffer << "    " << structName << "() {}\n";
    
    structBuffer << "};\n\n";
    
    generatedStructs.insert(signature);
    return structName;
}

